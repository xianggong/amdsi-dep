package amdsidep

import (
	"fmt"
	"strings"
)

type InstMisc struct {
	Type      string // Type of instruction
	IsMemInst bool   // Is a memory instruction ?
	DstSpReg  int    // Implicit def of special registers
	SrcSpReg  int    // Implicit use of special registers
}

func GetInstMisc(key string) *InstMisc {
	if val, ok := InstsMisc[key]; ok {
		return &val
	} else {
		fmt.Println("No ", key)
	}

	return nil
}

// GetType returns the type of an instruction
func GetInstType(key string) string {
	if val, ok := InstsMisc[key]; ok {
		return val.Type
	}
	return "Invalid"
}

// IsValidInst check if the assembly contains a valid instruction
func IsValidInst(asm string) bool {
	inputField := strings.FieldsFunc(asm, delimiter)
	if len(inputField) > 0 {
		inst := inputField[0]
		if _, ok := InstsMisc[inst]; ok {
			return true
		}
	}

	return false
}

func IsMemoryInstruction(key string) bool {
	if val, ok := InstsMisc[key]; ok {
		return val.IsMemInst
	}

	return false
}

